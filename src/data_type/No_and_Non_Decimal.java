package data_type;

public class No_and_Non_Decimal {

	public static void main(String[] args) {
		// Variable Declearation
		byte no;
		// Declear Value
		no =26;
		//usage
		System.out.println("No of Alphabets in English-         "+no);
		
		// Variable Declearation
		short no1;
		// Declear Value
		no1 =1500;
		//usage
		System.out.println("Distance from Pune To Delhi-        "+no1);
		
		// Variable Declearation
		int no2;
		// Declear Value
		no2 =1500000000;
		//usage
		System.out.println("Population of India-                "+no2);

	}

}
