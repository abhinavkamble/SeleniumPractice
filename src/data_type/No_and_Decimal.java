package data_type;

public class No_and_Decimal {

	public static void main(String[] args) {
		// Variable Declearation
		float No;
		// Declear Value
		No =9.81f;
		// Usage
		System.out.println("Value of Gravity:-   "+No+"m/s2");
	
		// Variable Declearation
		double No1;
		// Declear Value
		No1 =200.45162856187;
		// Usage
		System.out.println("Weight of Bullet:-   " +No1+ "kg");		

	}

}
